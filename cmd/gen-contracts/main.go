// Command gen-contracts regenerates web/src/lib/generated/contracts.ts from the Go
// contracts: the Job/Enrichment wire structs (via tygo) and the closed vocabularies
// (source/stages/enrichment facet values). Run it with `make gen-contracts`; the
// output is committed, so the build never depends on this tool.
package main

//go:generate go run .

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/gzuidhof/tygo/tygo"

	"github.com/strelov1/freehire/internal/enrich"
	"github.com/strelov1/freehire/internal/sources"
	"github.com/strelov1/freehire/internal/userjob"
)

const outputPath = "web/src/lib/generated/contracts.ts"

const header = "// Code generated by cmd/gen-contracts. DO NOT EDIT.\n" +
	"// Regenerate with `make gen-contracts`.\n\n"

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "gen-contracts:", err)
		os.Exit(1)
	}
}

func run() error {
	structs, err := genStructs()
	if err != nil {
		return err
	}

	var b strings.Builder
	b.WriteString(header)
	b.WriteString(structs)
	b.WriteString("\n")
	b.WriteString(genVocab())

	if err := os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
		return err
	}
	return os.WriteFile(outputPath, []byte(b.String()), 0o644)
}

// genStructs runs tygo for Enrichment then Job (Job references Enrichment, so
// Enrichment is emitted first) into a temp dir, then returns their concatenated
// bodies with tygo's per-file banner stripped.
func genStructs() (string, error) {
	tmp, err := os.MkdirTemp("", "gen-contracts")
	if err != nil {
		return "", err
	}
	defer os.RemoveAll(tmp)

	enrichTS := filepath.Join(tmp, "enrich.ts")
	jobviewTS := filepath.Join(tmp, "jobview.ts")
	verdictTS := filepath.Join(tmp, "verdict.ts")

	cfg := &tygo.Config{
		Packages: []*tygo.PackageConfig{
			{
				Path:         "github.com/strelov1/freehire/internal/enrich",
				OutputPath:   enrichTS,
				IncludeFiles: []string{"enrichment.go"},
			},
			{
				Path:         "github.com/strelov1/freehire/internal/jobview",
				OutputPath:   jobviewTS,
				IncludeFiles: []string{"jobview.go"},
				TypeMappings: map[string]string{"enrich.Enrichment": "Enrichment"},
			},
			{
				// The résumé-verdict wire shape (Verdict + Skill). Self-contained —
				// only primitives and []Skill, no cross-package references.
				Path:         "github.com/strelov1/freehire/internal/verdict",
				OutputPath:   verdictTS,
				IncludeFiles: []string{"verdict.go"},
			},
		},
	}
	if err := tygo.New(cfg).Generate(); err != nil {
		return "", fmt.Errorf("tygo: %w", err)
	}

	enrichBody, err := readBody(enrichTS)
	if err != nil {
		return "", err
	}
	jobviewBody, err := readBody(jobviewTS)
	if err != nil {
		return "", err
	}
	verdictBody, err := readBody(verdictTS)
	if err != nil {
		return "", err
	}
	return enrichBody + "\n" + jobviewBody + "\n" + verdictBody, nil
}

// readBody returns a tygo output file's body with its leading preamble removed, so
// the assembled file carries one banner. tygo prefixes each file with a fixed block:
// the "Code generated by tygo" banner, a "//////////" rule, a "// source: <file>"
// marker, and the Go package doc as a /* … */ comment. We drop everything up to the
// first non-comment, non-blank line — the JSDoc that documents the actual type is
// part of the export it precedes and is kept.
func readBody(path string) (string, error) {
	raw, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}
	lines := strings.Split(string(raw), "\n")
	i := skipPreamble(lines)
	return strings.TrimRight(strings.Join(lines[i:], "\n"), "\n") + "\n", nil
}

// skipPreamble returns the index of the first line of real TypeScript, skipping the
// tygo banner/source markers, the package-doc /* … */ block, and any blank lines —
// but stopping at the /** … */ JSDoc that documents an exported type.
func skipPreamble(lines []string) int {
	i := 0
	for i < len(lines) {
		line := strings.TrimSpace(lines[i])
		switch {
		case line == "":
			i++
		case strings.HasPrefix(line, "// Code generated") || strings.HasPrefix(line, "//////////") ||
			strings.HasPrefix(line, "// source:"):
			i++
		case strings.HasPrefix(line, "/*") && !strings.HasPrefix(line, "/**"):
			// Package-doc block comment: skip to its closing */.
			for i < len(lines) && !strings.HasSuffix(strings.TrimSpace(lines[i]), "*/") {
				i++
			}
			i++ // consume the closing */ line
		default:
			return i
		}
	}
	return i
}

// genVocab emits every closed vocabulary. Source = non-boardless providers plus the
// non-adapter sources set by other writers: "telegram" (the tg-extract worker) and the
// moderator/bulk-import origins ("workatastartup", "remoteok", "arc"). Stages from
// userjob; the rest from the enrich controlled vocabularies.
//
// An origin that started as a manual/bulk-import name can later gain a registered
// adapter (e.g. remoteok), so it appears in both the hardcoded prefix and
// FilterableProviders — dedup to keep SOURCE_VALUES a set. A duplicate value
// otherwise reaches the SPA's keyed Source facet and throws each_key_duplicate.
func genVocab() string {
	source := dedup(append([]string{"telegram", "workatastartup", "remoteok", "arc"}, sources.FilterableProviders()...))

	var b strings.Builder
	b.WriteString(emitVocab("Source", "SOURCE_VALUES", source))
	b.WriteString(emitVocab("Stage", "STAGE_VALUES", userjob.Stages))
	b.WriteString(emitVocab("WorkMode", "WORK_MODE_VALUES", enrich.WorkModeValues))
	b.WriteString(emitVocab("Seniority", "SENIORITY_VALUES", enrich.SeniorityValues))
	b.WriteString(emitVocab("Category", "CATEGORY_VALUES", enrich.CategoryValues))
	b.WriteString(emitVocab("EmploymentType", "EMPLOYMENT_TYPE_VALUES", enrich.EmploymentTypeValues))
	b.WriteString(emitVocab("Relocation", "RELOCATION_VALUES", enrich.RelocationValues))
	b.WriteString(emitVocab("EnglishLevel", "ENGLISH_LEVEL_VALUES", enrich.EnglishLevelValues))
	b.WriteString(emitVocab("CompanyType", "COMPANY_TYPE_VALUES", enrich.CompanyTypeValues))
	b.WriteString(emitVocab("Domain", "DOMAIN_VALUES", enrich.DomainValues))
	return b.String()
}

// dedup returns the slice with duplicates removed, preserving first-occurrence order.
func dedup(values []string) []string {
	seen := make(map[string]struct{}, len(values))
	out := values[:0]
	for _, v := range values {
		if _, ok := seen[v]; ok {
			continue
		}
		seen[v] = struct{}{}
		out = append(out, v)
	}
	return out
}
